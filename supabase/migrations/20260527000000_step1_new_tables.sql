ALTER TABLE goal_accounts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "goal_accounts: own rows select (temporary, replaced in step 2)"
  ON goal_accounts FOR SELECT
  USING (user_id = auth.uid());

CREATE POLICY "goal_accounts: own rows insert (temporary, replaced in step 2)"
  ON goal_accounts FOR INSERT
  WITH CHECK (user_id = auth.uid());

CREATE POLICY "goal_accounts: own rows delete (temporary, replaced in step 2)"
  ON goal_accounts FOR DELETE
  USING (user_id = auth.uid());

-- ============================================================
-- RLS — budget_daily_snapshots
-- All writes are service role only (nightly cron).
-- SELECT policy references the new budgets table which doesn't exist yet —
-- added in Step 3. Until then, no authenticated reads (service role reads all).
-- ============================================================

ALTER TABLE budget_daily_snapshots ENABLE ROW LEVEL SECURITY;

CREATE POLICY "snapshots: no authenticated access (service role only)"
  ON budget_daily_snapshots FOR SELECT
  USING (false);

-- No INSERT / UPDATE / DELETE policies for authenticated users.

